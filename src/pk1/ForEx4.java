package pk1;

public class ForEx4 {

	public static void main(String[] args) {
			int[] ints = {1,2,3,4};	
		  // int[] ints = new int[]{1,2,3,4};
		  
		  
		  for (int i = 0; i < ints.length; i++) {
			System.out.println(ints[i]);
		}
		  for (int data : ints) {
			  System.out.println(data);			
		}

	}

}
